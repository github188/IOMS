package cn.com.atnc.ioms.model.clientnotice;

import cn.com.atnc.ioms.entity.BaseUserInfo;

/**
 * @author:lizhisheng
 * @date:2014-4-21 上午9:17:21 存放发送消息的2个参数 user 当前用户 msg 消息
 * @version:1.0
 */
public class Message {
	private BaseUserInfo user;
	private String msg;

	public Message() {

	}

	public Message(BaseUserInfo user, final String msg) {
		this.user = user;
		this.msg = msg;
	}

	public BaseUserInfo getUser() {
		return user;
	}

	public void setUser(BaseUserInfo user) {
		this.user = user;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}

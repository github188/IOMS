package cn.com.atnc.ioms.model.operstat.atm;

import cn.com.atnc.ioms.entity.operstat.atm.Card;

/**
 * ATM数据网节点统计DBmodel
 * @author：KuYonggang
 * @date：2014-8-29下午2:11:20
 * @version：1.0
 */
public class NodeStatDBModel {
	
	private String cardSlot;
	private String cardFbState;
	private String cardAlarmStatus;
	private Card card;
	private String nodeSystemUptime;
	
	/**
	 * @param cardSlot
	 * @param cardFbState
	 * @param cardAlarmStatus
	 * @param card
	 * @param nodeSystemUptime
	 */
	public NodeStatDBModel(String cardSlot, String cardFbState,
			String cardAlarmStatus, Card card, String nodeSystemUptime) {
		super();
		this.cardSlot = cardSlot;
		this.cardFbState = cardFbState;
		this.cardAlarmStatus = cardAlarmStatus;
		this.card = card;
		this.nodeSystemUptime = nodeSystemUptime;
	}
	public String getCardSlot() {
		return cardSlot;
	}
	public void setCardSlot(String cardSlot) {
		this.cardSlot = cardSlot;
	}
	public String getCardFbState() {
		return cardFbState;
	}
	public void setCardFbState(String cardFbState) {
		this.cardFbState = cardFbState;
	}
	public String getCardAlarmStatus() {
		return cardAlarmStatus;
	}
	public void setCardAlarmStatus(String cardAlarmStatus) {
		this.cardAlarmStatus = cardAlarmStatus;
	}
	public Card getCard() {
		return card;
	}
	public void setCard(Card card) {
		this.card = card;
	}
	public String getNodeSystemUptime() {
		return nodeSystemUptime;
	}
	public void setNodeSystemUptime(String nodeSystemUptime) {
		this.nodeSystemUptime = nodeSystemUptime;
	}
	
}

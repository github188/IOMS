package cn.com.atnc.ioms.model.phonemng;

public class CountCallModel {
	private String userName;//坐席对应工程师
	private String atncType;//电话类型
	private transient double sumtimes;
	
	
	
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getAtncType() {
		return atncType;
	}
	public void setAtncType(String atncType) {
		this.atncType = atncType;
	}
	public double getSumtimes() {
		return sumtimes;
	}
	public void setSumtimes(double sumtimes) {
		this.sumtimes = sumtimes;
	}
	
}

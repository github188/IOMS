package cn.com.atnc.ioms.entity.reports;


/**
 * 转报地址电路申请实体
 * @author WangLingbin
 * @date 2015年10月23日 下午3:23:42
 * @since 1.0.0
 */
public class TeleGraphCircuitEntity {

	private String num;						//序号
	private String applyUserArea;			//使用地点
	private String channelName;		//信道名称
	private String inWord;			//入冠字
	private String outWord;			//出冠字
	private String alarmAddress;	//告警地址
	private String masthead;		//报头
	private String trailer;			//报尾
	private String speed;			//信道速率
	private String code;			//码制
	private String queue;			//所属队列
	public TeleGraphCircuitEntity() {
		super();
	}
	public TeleGraphCircuitEntity(String num, String applyUserArea,
			String channelName, String inWord, String outWord,
			String alarmAddress, String masthead, String trailer, String speed,
			String code, String queue) {
		super();
		this.num = num;
		this.applyUserArea = applyUserArea;
		this.channelName = channelName;
		this.inWord = inWord;
		this.outWord = outWord;
		this.alarmAddress = alarmAddress;
		this.masthead = masthead;
		this.trailer = trailer;
		this.speed = speed;
		this.code = code;
		this.queue = queue;
	}

	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getApplyUserArea() {
		return applyUserArea;
	}
	public void setApplyUserArea(String applyUserArea) {
		this.applyUserArea = applyUserArea;
	}
	public String getChannelName() {
		return channelName;
	}
	public void setChannelName(String channelName) {
		this.channelName = channelName;
	}
	public String getInWord() {
		return inWord;
	}
	public void setInWord(String inWord) {
		this.inWord = inWord;
	}
	public String getOutWord() {
		return outWord;
	}
	public void setOutWord(String outWord) {
		this.outWord = outWord;
	}
	public String getAlarmAddress() {
		return alarmAddress;
	}
	public void setAlarmAddress(String alarmAddress) {
		this.alarmAddress = alarmAddress;
	}
	public String getMasthead() {
		return masthead;
	}
	public void setMasthead(String masthead) {
		this.masthead = masthead;
	}
	public String getTrailer() {
		return trailer;
	}
	public void setTrailer(String trailer) {
		this.trailer = trailer;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getQueue() {
		return queue;
	}
	public void setQueue(String queue) {
		this.queue = queue;
	}
	
}

public enum SiteType {

	undefined("undefined"), standard("standard"), v2("v2"), phonelink("phonelink"), kuefdata(
			"kuefdata"), kuanacom("kuanacom"),kuatnc("kuatnc");
	private String value;

	SiteType(String value) {
		this.value = value;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String toString() {
		return value;
	}
}

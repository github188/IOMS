package cn.com.atnc.ioms.enums.clientnotice;

/**
 * 类说明:客户通知发送状态枚举类
 * 
 * @author lizhisheng
 * @date 2014-4-16 下午6:20:20
 */
public enum NoticeState {
	SUCCESS("发送成功"), SMSFAIL("短信发送失败"), EMAILFAIL("邮件发送失败"), ALLFAIL("均发送失败");

	private String name;

	public String getName() {
		return name;
	}

	private NoticeState(String name) {
		this.name = name;
	}
}

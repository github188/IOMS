package cn.com.atnc.ioms.enums.faultmng;

/**
 * 北京地区故障归类种类
 * 
 * @author duanyanlin 2016年6月17日 下午2:03:29
 */
public enum BJFaultType {
	ATM("ATM专业故障"),

	SATELLITE("卫星专业故障"),

	TELEGRAPH("转报专业故障"),

	ENVIRONMENT("环境专业故障");

	private String value;

	private BJFaultType(String value) {
		this.value = value;
	}

	public String getName() {
		return name();
	}

	public String getValue() {
		return this.value;
	}
}
package cn.com.atnc.ioms.enums.tempcircuit;

/**
 * 类说明：电路撤销类型
 * 
 * @author: 局域网_王鹏
 * @time: 2014年3月26日下午6:03:32
 * @version:1.0
 */
public enum CircuitCancelType {
	NOTDELETE("未撤销"), APPLYDELETE("用户撤销"), FORCEDELETE("强制撤销");
	private String name;

	public String getName() {
		return name;
	}

	private CircuitCancelType(String name) {
		this.name = name;
	}
}
